public class M2_Implementing_for_loops {
    public static void main (String [] args ){
        int scoopsSold;
        /*For loop */
        for (scoopsSold = 0; scoopsSold <10 ; scoopsSold ++ ){
            System.out.println(scoopsSold);
        } /* Simple for loop to count form 0 to 9 */
    }
}
